"""
Publication registry for managing ingestion source configurations.

Two related tables:

- ``JOURNALS`` — one row per real-world journal, keyed by a stable
  opaque slug.  Owns the journal-level facts: display name, official
  website, ISSN(s), publisher, historical-name aliases.
- ``SOURCES`` — one row per ingestion mechanism (RSS / PMC /
  Crossref / local mirror) for a given journal.  Each row's
  ``journal`` field is a foreign-key slug into JOURNALS.

See ``docs/publications_metadata_consolidation.md``.
"""

import sys
from typing import Any, Dict, List, Optional, TypedDict

if sys.version_info >= (3, 11):
    from typing import NotRequired
else:  # pragma: no cover — only one branch is exercised at runtime
    from typing_extensions import NotRequired


class JournalEntry(TypedDict, total=False):
    """One row of the ``JOURNALS`` table.

    ``name`` is the only required field — every journal must
    have a display name.  All other fields are NotRequired
    because the data is genuinely heterogeneous (some journals
    have ISSN, some ISBN, some neither; eissn is electronic-
    only or absent on print-only journals).
    """
    name:      str
    aliases:   NotRequired[List[str]]
    address:   NotRequired[str]   # journal homepage (NOT scrape URL)
    issn:      NotRequired[str]
    eissn:     NotRequired[str]
    isbn:      NotRequired[str]   # for book-like references
    doi:       NotRequired[str]   # journal-DOI, not article DOI
    publisher: NotRequired[str]
    abbrev:    NotRequired[str]   # ISO 4 abbreviation


class SourceEntry(TypedDict, total=False):
    """One row of the ``SOURCES`` table — a single ingestion
    mechanism for some journal.  ``journal`` (the FK slug into
    JOURNALS), ``source``, and ``ingestor_class`` are the
    operational requirements.
    """
    journal:           str   # FK slug into JOURNALS
    name:              str   # legacy display name (kept for migration window)
    source:            str
    address:           NotRequired[str]   # scrape endpoint (NOT homepage)
    ingestor_class:    str
    local_path:        NotRequired[str]
    local_path_prefix: NotRequired[str]
    url_prefix:        NotRequired[str]
    rate_limit_min_ms: NotRequired[int]
    rate_limit_max_ms: NotRequired[int]
    issn:              NotRequired[str]
    eissn:             NotRequired[str]


# Publishers / source-providers that pre-refactor SOURCES rows
# tacked on as ``" (Publisher)"`` parentheticals to disambiguate
# multi-ingestor journals.  Stripping them is what causes legacy
# compound names like ``"Journal of Fungi (PMC)"`` to resolve to
# the same canonical journal as ``"Journal of Fungi"``.
_KNOWN_PUBLISHER_SUFFIXES = (
    'PMC',
    'Taylor & Francis',
    'Internet Archive',
)


def strip_publisher_suffix(name: str) -> str:
    """Strip a trailing ``" (Publisher)"`` when the publisher is a
    known disambiguator.  Idempotent; conservative — non-publisher
    parentheticals like ``"Mycotaxon (n.s.)"`` pass through
    unchanged.

    Also imported by ``bin/scaffold_journals.py`` (one source of
    truth for the recognised-publisher list).
    """
    stripped = name.strip()
    for suffix in _KNOWN_PUBLISHER_SUFFIXES:
        marker = f'({suffix})'
        if stripped.endswith(marker):
            return stripped[:-len(marker)].strip()
    return stripped


