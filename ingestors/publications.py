    @classmethod
    def get_all(cls) -> Dict[str, Dict[str, Any]]:
        """
        Get all publication source configurations.

        Each configuration includes default values merged with source-specific settings.

        Returns:
            Dictionary mapping publication keys to their configurations
        """
        result = {}
        for key, config in cls.SOURCES.items():
            cfg = cls.DEFAULTS.copy()
            cfg.update(config)
            result[key] = cfg
        return result

    @classmethod
    def get(cls, key: str) -> Optional[Dict[str, Any]]:
        """
        Get a specific publication configuration by key.

        Configuration includes default values merged with source-specific settings.

        Args:
            key: Publication key (e.g., 'mycotaxon', 'mycosphere')

        Returns:
            Publication configuration dict, or None if not found
        """
        config = cls.SOURCES.get(key)
        if config is None:
            return None
        cfg = cls.DEFAULTS.copy()
        cfg.update(config)
        return cfg

    @classmethod
    def get_robots_url(cls, source: str, custom_url: Optional[str] = None) -> str:
        """
        Get the robots.txt URL for a given source.

        Args:
            source: Name of the data source (e.g., 'ingenta', 'mycosphere')
            custom_url: Custom robots.txt URL (overrides default)

        Returns:
            URL to robots.txt file, or empty string if not found
        """
        if custom_url:
            return custom_url

        return cls.ROBOTS_URLS.get(source, '')

    @classmethod
    def list_publications(cls) -> list[str]:
        """
        Get a list of all available publication keys.

        Returns:
            Sorted list of publication keys
        """
        return sorted(cls.SOURCES.keys())

    @classmethod
    def get_defaults(cls) -> Dict[str, Any]:
        """
        Get default configuration values.

        Returns:
            Dictionary of default configuration values
        """
        return cls.DEFAULTS.copy()

    @classmethod
    def get_by_journal(cls, journal_name: str) -> Optional[Dict[str, Any]]:
        """
        Get the first publication configuration for a journal.

        Used to match documents (which have a 'journal' field that may
        still carry an old display name like 'Journal of Fungi (PMC)' or
        the canonical 'Journal of Fungi') to their source configuration.
        Post-refactor, ``SOURCES[*].journal`` is a slug FK into JOURNALS;
        this method resolves ``display_name → canonical_name → slug →
        first SOURCES entry pointing at that slug``.

        Args:
            journal_name: The journal name from a document (e.g.,
                'Mycotaxon', 'Journal of Fungi (PMC)',
                'Persoonia - Molecular Phylogeny and Evolution of Fungi')

        Returns:
            Publication configuration dict, or None if not found
        """
        slug = cls._find_slug_for_journal_name(journal_name)
        if slug is None:
            return None
        for key, config in cls.SOURCES.items():
            if config.get('journal') == slug:
                cfg = cls.DEFAULTS.copy()
                cfg.update(config)
                cfg['key'] = key  # source key for reference
                return cfg
        return None

    @classmethod
    def _find_slug_for_journal_name(
        cls, journal_name: str,
    ) -> Optional[str]:
        """Resolve a (possibly compound or aliased) journal display
        name to its canonical slug.  Order:

        1. Direct slug match (caller passed the slug itself).
        2. ``normalize_journal_name`` (strip publisher suffix +
           legacy alias resolution) → display-name lookup in JOURNALS.
        """
        if not journal_name:
            return None
        if journal_name in cls.JOURNALS:
            return journal_name
        canonical = cls.normalize_journal_name(journal_name)
        for slug, entry in cls.JOURNALS.items():
            if entry.get('name') == canonical:
                return slug
        return None

    @classmethod
    def get_journal(
        cls, slug_or_name: str,
    ) -> Optional[JournalEntry]:
        """Look up a JOURNALS entry by slug, display name, or known
        alias.

        Args:
            slug_or_name: One of:
                - a JOURNALS slug (``'sydowia'``);
                - a canonical display name (``'Sydowia'``);
                - a known alias (``'Sydowia Beih.'``) — resolved via
                  ``normalize_journal_name``.

        Returns:
            The JOURNALS entry, or None if the input doesn't resolve.
        """
        slug = cls._find_slug_for_journal_name(slug_or_name)
        if slug is None:
            return None
        return cls.JOURNALS[slug]

    @classmethod
    def find_journal_by_issn(
        cls, issn: Optional[str],
    ) -> Optional[str]:
        """Find the JOURNALS slug for a given ISSN (matches either the
        primary ``issn`` field or the ``eissn`` field).  Returns None
        for empty input or no match."""
        if not issn:
            return None
        for slug, entry in cls.JOURNALS.items():
            if entry.get('issn') == issn or entry.get('eissn') == issn:
                return slug
        return None

    @classmethod
    def find_journal_by_doi(
        cls, doi: Optional[str],
    ) -> Optional[str]:
        """Find the JOURNALS slug whose journal-level ``doi`` field
        matches.  Per-article DOIs are out of scope — those need a
        Crossref lookup, not a JOURNALS scan.  Returns None for empty
        input or no match."""
        if not doi:
            return None
        for slug, entry in cls.JOURNALS.items():
            if entry.get('doi') == doi:
                return slug
        return None

    @classmethod
    def find_journal_by_isbn(
        cls, isbn: Optional[str],
    ) -> Optional[str]:
        """Find the JOURNALS slug whose ``isbn`` field matches —
        used for book-like reference works (e.g. Funga Nordica
        chapters once they're registered in JOURNALS).  Returns None
        for empty input or no match."""
        if not isbn:
            return None
        for slug, entry in cls.JOURNALS.items():
            if entry.get('isbn') == isbn:
                return slug
        return None

    # Cache: ``{alias_value: canonical_name}`` index built from
    # every ``JOURNALS[*].aliases`` list on first call.
    _ALIAS_INDEX: Optional[Dict[str, str]] = None

    @classmethod
    def _build_alias_index(cls) -> Dict[str, str]:
        """Walk ``JOURNALS`` and flatten every entry's ``aliases``
        list into a ``{alias: canonical_name}`` lookup table.

        Cached on the class — invalidates only when the class is
        reloaded.  This is the post-phase-3 replacement for the
        legacy ``JOURNAL_NAME_ALIASES`` dict.
        """
        index: Dict[str, str] = {}
        for entry in cls.JOURNALS.values():
            canonical = entry.get('name')
            if not canonical:
                continue
            for alias in entry.get('aliases', []) or []:
                index[alias] = canonical
        return index

    @classmethod
    def normalize_journal_name(cls, journal_name: str) -> str:
        """
        Normalize a journal name to its canonical form.

        Two-step:
        1. Strip a known publisher-disambiguating parenthetical
           suffix (``" (PMC)"`` etc.) — see
           :func:`strip_publisher_suffix`.
        2. Scan ``JOURNALS[*].aliases``: if the stripped name
           matches an alias, return the owning journal's canonical
           ``name``.

        Args:
            journal_name: The journal name to normalize

        Returns:
            The canonical journal name (or the original if no
            normalization applies)
        """
        stripped = strip_publisher_suffix(journal_name)
        if cls._ALIAS_INDEX is None:
            cls._ALIAS_INDEX = cls._build_alias_index()
        return cls._ALIAS_INDEX.get(stripped, stripped)
